export const getProductBySlug = (slug) => {
    return products.find((product) => product.slug === slug);
};

export const getProductsByCategory = (category) => {
    const categoryLower = category.toLowerCase().replace(/-/g, ' ');
    return products.filter(
        (product) => product.category.toLowerCase().replace(/-/g, ' ') === categoryLower
    );
};

export const getProductsByGender = (gender) => {
    if (gender === 'kids') {
        return products.filter((product) => product.gender === 'kids');
    }
    return products.filter(
        (product) => product.gender === gender || product.gender === 'unisex'
    );
};

export const getFeaturedProducts = () => {
    return products.filter((product) => product.badges?.includes('BESTSELLER'));
};

export const getNewArrivals = () => {
    return products.filter((product) => product.badges?.includes('NEW'));
};

export const getSaleProducts = () => {
    return products.filter((product) => product.badges?.includes('SALE'));
};

export const getTrendingProducts = () => {
    return products.filter((product) => product.badges?.includes('TRENDING'));
};

export const getProductsBySubcategory = (subcategory) => {
    return products.filter(
        (product) => product.subcategory.toLowerCase() === subcategory.toLowerCase()
    );
};
